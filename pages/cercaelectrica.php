        <h4 class="titulo-page-informacion">Cerca El&eacute;ctrica</h4>                    
        <p class="texto-parrafo">
          
            La <strong>cerca el&eacute;ctrica</strong>  es un sistema de prevenci&oacute;n  de tentativas 
            que permite detectar o repeler intrusos sin  intervenci&oacute;n 
            humana, el equipo emite pulsos de energ&iacute;a controlada 
            que circulan a trav&eacute;s de hilos especiales, al hacer 
            contacto el intruso con la cerca electrifica , recibe 
            una descarga   repeliendo su accionar y alej&aacute;ndolo 
            r&aacute;pidamente sin producirles da&ntilde;os fatales.            
        </p>
        <p class="texto-parrafo">
            <div align="center" class="img-topografo">
                <img src="img/info/ssp-cerca-electrica.png"  title="Cerca Electrica" alt="Cerca Electrica" width="85%" height="45%" />
            </div>
        </p>
        <h6 class="titulo-page-informacion">Preguntas frecuentes sobre Cerca El&eacute;ctrica</h6>
        
        <h6 class="titulo-page-informacion">¿C&oacute;mo funciona una cerca el&eacute;ctrica?</h6>        
        <p class="texto-parrafo">
             
            <img src="img/info/ladrones-servicios-electrificador.jpg"  title="Electrificador de Cerca" alt="Electrificador de Cerca"  width="230px" height="210px" style="float: left; padding-left: 5px; padding-right: 5px;" />
            
            Este sistema cuenta con un energizador de 8000 y 12000 
            voltios lo cual genera una gran descarga el&eacute;ctrica a 
            cualquier persona que intente tocarla o da&ntilde;arla. 
            Se alimenta de corriente el&eacute;ctrica del domicilio 
            y cuando esta llega a fallar nuestro energizador 
            sigue funcionado ya que cuenta con una batería 
            de respaldo lo cual servir&aacute; para que la cerca 
            siga electrificada por aproximadamente 12 a 24 hrs.
            
        </p>
        
        <h6 class="titulo-page-informacion">¿Puedo activar mi cerca el&eacute;ctrica d&iacute;a y noche?</h6>
        
        <p class="texto-parrafo">
            
            El  equipo est&aacute; dise&ntilde;ado para trabajar las 24 hrs ya 
            que cuenta con un control remoto de encendido y apagado, 
            esto es con la finalidad de poder desergenizar la cerca 
            cuando usted vaya hacer algún mantenimiento por ejemplo, 
            adornos  de bardas, pintado de paredes etc.
            
        </p>              
        
        <h6 class="titulo-page-informacion">¿Qu&eacute; pasa si un intruso da&ntilde;a la cerca electrificada?</h6>
        
        <p class="texto-parrafo">
            
            Si la cerca electrificada llegara a ser da&ntilde;ada en 
            cualquiera de sus puntos inmediatamente se activar&aacute; 
            la alarma que consta de 1 sirena de 30 watts, 
            lo cual causar&aacute; aturdimiento y p&aacute;nico al intruso 
            oblig&aacute;ndolo a alejarse del lugar inmediatamente 
            y al mismo tiempo le indicara a usted y sus 
            vecinos que algo extra&ntilde;o est&aacute; pasando en su domicilio.
            
        </p>
        
        <h6 class="titulo-page-informacion">¿Con una cerca electrificada peligra la vida de un ser humano?</h6>
        
        <p class="texto-parrafo">
            
           Dado a que este sistema de seguridad cuenta con dispositivos 
           electr&oacute;nicos el intruso &uacute;nicamente recibir&aacute; la descarga 
           el&eacute;ctrica necesaria para adormecer el musculo por 
           un breve momento y obligarlo a soltar la cerca 
           electrificada.
            
        </p>
        
        <h6 class="titulo-page-informacion">¿C&oacute;mo determino el n&uacute;mero de l&iacute;neas y la altura de mi cerca?</h6>
        
        <p class="texto-parrafo">
            
           Esto se establece conforme a las necesidades del inmueble, 
           se determina la altura y cantidad de l&iacute;neas energizadas 
           que se necesitan para hacer una mejor protecci&oacute;n del lugar.
           
        </p>
        
        <h6 class="titulo-page-informacion">¿Si cuento con cerca el&eacute;ctrica se 
            reflejara considerablemente en mi recibo de pago de consumo el&eacute;ctrico?</h6>
        
            <p class="texto-parrafo">

                Debido a la tecnolog&iacute;a de punta con que el sistema est&aacute; 
                dise&ntilde;ado es apto para funcionar las 24 hrs. Y el 
                consumo de corriente el&eacute;ctrica es m&iacute;nimo, en promedio 
                Bs. 8 a 10 mensual.

            </p>
